%梯度下降法求发f(x) = 5 * x.^2 - 3 * x -100;在[-10,10]的最小值
clear
clc
x = -10:0.1:10;
y = 5 * x.^2 - 3 * x - 100;
plot(x,y,'LineWidth',2)
hold on
n = 1
l = 0.05;
x0 = 10;
y0 = 5* x0.^2 - 3 * x0 - 100;
y1 = inf;
while abs(y0-y1)>0.01
    x1 =  x0 - l * (10* x0 - 3);
    y0 = 5* x0.^2 - 3*x0 - 100;
    y1 = 5* x1.^2 - 3*x1 - 100;
    plot([x0,x1],[y0,y1],'r')
    x0 = x1;
    n = n+1
end
y1
title('梯度下降法求发f(x) = 5 * x.^2 - 3 * x -100;在[-10,10]的极小值')